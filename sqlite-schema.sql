);

CREATE TABLE IF NOT EXISTS Voyages (
  sid INTEGER NOT NULL,
  bid INTEGER NOT NULL,
  date_of_voyage DATE NOT NULL,
  PRIMARY KEY(sid, bid, date_of_voyage)
);